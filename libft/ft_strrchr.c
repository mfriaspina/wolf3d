#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int		i;

	i = ft_strlen(s);
	while (i != -1 && s[i] != c)
		i--;
	if (i == -1)
		return (NULL);
	else
		return ((char *)&s[i]);
}
