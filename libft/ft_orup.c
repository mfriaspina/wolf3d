#include "libft.h"

int	ft_orup(char a, char c)
{
	if (c == a)
		return (1);
	c = (c >= 'a' && c <= 'z') ? ft_toupper(c) : ft_tolower(c);
	if (c == a)
		return (1);
	return (0);
}
